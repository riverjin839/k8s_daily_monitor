"""
Ansible Playbook мӢӨн–үкё°

мӢӨн–ү лӘЁл“ң:
  - local: subprocessлЎң м§Ғм ‘ мӢӨн–ү (kind/dev нҷҳкІҪ)
  - ssh:   master#1м—җ SSH м ‘мҶҚн•ҳм—¬ мӢӨн–ү (production, лҜёкө¬нҳ„ вҶ’ TODO)

мғҒнғң л§Өн•‘ (ansible JSON callback м¶ңл Ҙ кё°л°ҳ):
  - лӘЁл“  нҳёмҠӨнҠё failures=0, unreachable=0  вҶ’ healthy (мҙҲлЎқ)
  - м–ҙлҠҗ нҳёмҠӨнҠёл“  failures > 0             вҶ’ critical (л№Ёк°•)
  - к·ё мҷё (unreachable, changed л“ұ)         вҶ’ warning (мЈјнҷ©)
"""
import json
import os
import re
import subprocess
import tempfile
from datetime import datetime
from typing import Any

from app.config import settings


class PlaybookResult:
    def __init__(
        self,
        status: str,
        message: str,
        duration_ms: int = 0,
        stats: dict[str, Any] | None = None,
        raw_output: str = "",
    ):
        self.status = status
        self.message = message
        self.duration_ms = duration_ms
        self.stats = stats or {}
        self.raw_output = raw_output


def _write_dynamic_inventory(hosts: list[str]) -> str:
    """лҸҷм Ғ inventory нҢҢмқјмқ„ мһ„мӢңлЎң мғқм„ұ. нҳёмҠӨнҠёлҠ” `[all]` к·ёлЈ№м—җ лӢЁмҲң лӮҳм—ҙ.

    нҳёмҠӨнҠё ліҖмҲҳ(ansible_user / ansible_ssh_pass / ansible_ssh_private_key_file л“ұ)лҠ”
    playbook.extra_vars лЎң `-e` м „лӢ¬лҸј group_vars мІҳлҹј м Ғмҡ©лҗңлӢӨ.
    нҳёмҠӨнҠёнӮӨ кІҖмӮ¬лҠ” мӮ¬лӮҙл§қ м „м ңлЎң л№„нҷңм„ұ вҖ” Settings лЎң л…ём¶ңн•ҳкі  мӢ¶мңјл©ҙ м¶”нӣ„ л¶„лҰ¬.

    нҢҢмқјмқҖ нҳём¶ңмһҗк°Җ м •лҰ¬(unlink)н•  мұ…мһ„. (run_playbook к°Җ finally м—җм„ң мӮӯм ң.)
    """
    # shell injection / мһҳлӘ»лҗң нҶ нҒ° м°ЁлӢЁ вҖ” IPv4 / IPv6 / FQDN л§Ң н—Ҳмҡ©
    safe = [h for h in (hosts or []) if h and re.match(r"^[A-Za-z0-9_.:\-\[\]]+$", h)]
    fd, path = tempfile.mkstemp(prefix="k8s-monitor-inv-", suffix=".ini")
    with os.fdopen(fd, "w") as f:
        f.write("# auto-generated by k8s_daily_monitor вҖ” cluster nodes from K8s API\n")
        f.write("[all]\n")
        for h in safe:
            f.write(f"{h}\n")
        f.write("\n[all:vars]\n")
        f.write("ansible_host_key_checking=False\n")
        f.write("ansible_ssh_common_args='-o StrictHostKeyChecking=no'\n")
    return path


def run_playbook(
    playbook_path: str,
    inventory_path: str | None = None,
    extra_vars: dict[str, Any] | None = None,
    tags: str | None = None,
    timeout: int | None = None,
    inventory_hosts: list[str] | None = None,
) -> PlaybookResult:
    """ansible-playbookмқ„ JSON callbackкіј н•Ёк»ҳ мӢӨн–үн•ҳкі  кІ°кіјлҘј нҢҢмӢұн•©лӢҲлӢӨ.

    inventory мҡ°м„ мҲңмң„:
      1) `inventory_path` (мӮ¬мҡ©мһҗк°Җ лӘ…мӢңм ҒмңјлЎң м§Җм •н•ң нҢҢмқј кІҪлЎң)
      2) `inventory_hosts` (нҳём¶ңмһҗк°Җ K8s API л“ұм—җм„ң к°Җм ёмҳЁ л…ёл“ң лҰ¬мҠӨнҠё вҖ” мһ„мӢң нҢҢмқј мһҗлҸҷмғқм„ұ)
      3) м •м Ғ fallback `{ansible_inventory_dir}/clusters.yml`
    """

    cmd = ["ansible-playbook", playbook_path]

    # inventory вҖ” мӮ¬мҡ©мһҗк°Җ лӘ…мӢңн•ң кІҢ мһҲмңјл©ҙ к·ёкІғмқ„ мӮ¬мҡ©, м—Ҷмңјл©ҙ K8s л…ёл“ң лҰ¬мҠӨнҠёлЎң лҸҷм Ғ мғқм„ұ.
    # лҸҷм Ғ мғқм„ұ мӢң мһ„мӢң нҢҢмқјмқ„ л§Ңл“Өкі  finally м—җм„ң м •лҰ¬н•ңлӢӨ.
    temp_inventory: str | None = None
    if inventory_path:
        cmd.extend(["-i", inventory_path])
    elif inventory_hosts:
        temp_inventory = _write_dynamic_inventory(inventory_hosts)
        cmd.extend(["-i", temp_inventory])
    else:
        default_inv = f"{settings.ansible_inventory_dir}/clusters.yml"
        cmd.extend(["-i", default_inv])

    # extra vars
    if extra_vars:
        cmd.extend(["-e", json.dumps(extra_vars)])

    # tags
    if tags:
        cmd.extend(["--tags", tags])

    env = {
        "ANSIBLE_STDOUT_CALLBACK": "json",
        "ANSIBLE_LOAD_CALLBACK_PLUGINS": "1",
        "PATH": "/usr/local/sbin:/usr/local/bin:/usr/sbin:/usr/bin:/sbin:/bin",
        "HOME": "/root",
    }

    effective_timeout = timeout or settings.check_timeout_seconds

    start = datetime.utcnow()
    try:
        result = subprocess.run(
            cmd,
            capture_output=True,
            text=True,
            timeout=effective_timeout,
            env=env,
        )
        elapsed = int((datetime.utcnow() - start).total_seconds() * 1000)
        return _parse_result(result, elapsed)

    except subprocess.TimeoutExpired:
        elapsed = int((datetime.utcnow() - start).total_seconds() * 1000)
        return PlaybookResult(
            status="critical",
            message=f"Playbook timed out after {effective_timeout}s",
            duration_ms=elapsed,
        )
    except FileNotFoundError:
        return PlaybookResult(
            status="critical",
            message="ansible-playbook command not found. Install ansible in the execution environment.",
            duration_ms=0,
        )
    except Exception as e:
        elapsed = int((datetime.utcnow() - start).total_seconds() * 1000)
        return PlaybookResult(
            status="critical",
            message=f"Execution error: {str(e)}",
            duration_ms=elapsed,
        )
    finally:
        if temp_inventory:
            try:
                os.unlink(temp_inventory)
            except OSError:
                pass


def _parse_result(result: subprocess.CompletedProcess, elapsed: int) -> PlaybookResult:
    """ansible JSON callback м¶ңл Ҙмқ„ нҢҢмӢұн•ҳм—¬ мғҒнғңлҘј кІ°м •н•©лӢҲлӢӨ."""
    raw = result.stdout or ""

    # JSON нҢҢмӢұ мӢңлҸ„
    try:
        data = json.loads(raw)
    except (json.JSONDecodeError, ValueError):
        # JSONмқҙ м•„лӢҢ кІҪмҡ° returncode кё°л°ҳ нҢҗлӢЁ
        if result.returncode == 0:
            return PlaybookResult(
                status="healthy",
                message="Playbook completed successfully (non-JSON output)",
                duration_ms=elapsed,
                raw_output=raw[:2000],
            )
        return PlaybookResult(
            status="critical",
            message=f"Playbook failed (rc={result.returncode}): {result.stderr[:500]}",
            duration_ms=elapsed,
            raw_output=raw[:2000],
        )

    # stats нҢҢмӢұ
    stats = data.get("stats", {})
    if not stats:
        # statsк°Җ м—Ҷмңјл©ҙ returncode кё°л°ҳ
        status = "healthy" if result.returncode == 0 else "critical"
        return PlaybookResult(
            status=status,
            message="Playbook completed" if status == "healthy" else "Playbook failed",
            duration_ms=elapsed,
            stats=stats,
            raw_output=raw[:2000],
        )

    # нҳёмҠӨнҠёлі„ нҶөкі„ м§‘кі„
    total_ok = 0
    total_changed = 0
    total_failures = 0
    total_unreachable = 0
    total_skipped = 0
    host_details = {}

    for host, host_stats in stats.items():
        ok = host_stats.get("ok", 0)
        changed = host_stats.get("changed", 0)
        failures = host_stats.get("failures", 0)
        unreachable = host_stats.get("unreachable", 0)
        skipped = host_stats.get("skipped", 0)

        total_ok += ok
        total_changed += changed
        total_failures += failures
        total_unreachable += unreachable
        total_skipped += skipped

        host_details[host] = {
            "ok": ok,
            "changed": changed,
            "failures": failures,
            "unreachable": unreachable,
            "skipped": skipped,
        }

    # мғҒнғң кІ°м •
    if total_failures > 0:
        status = "critical"
        message = f"Failed: {total_failures} task(s) failed across {len(stats)} host(s)"
    elif total_unreachable > 0:
        status = "warning"
        message = f"Warning: {total_unreachable} host(s) unreachable"
    elif total_changed > 0:
        status = "warning"
        message = f"Changed: {total_changed} task(s) changed across {len(stats)} host(s)"
    else:
        status = "healthy"
        message = f"OK: All {total_ok} task(s) passed across {len(stats)} host(s)"

    summary_stats = {
        "hosts": host_details,
        "totals": {
            "ok": total_ok,
            "changed": total_changed,
            "failures": total_failures,
            "unreachable": total_unreachable,
            "skipped": total_skipped,
            "host_count": len(stats),
        },
    }

    return PlaybookResult(
        status=status,
        message=message,
        duration_ms=elapsed,
        stats=summary_stats,
        raw_output=raw[:5000],
    )
